# ── RAMSES-RT on the Iliev Test-1 problem (ADR-0006 Phase 4, flagship 2 core) ─
#
# The SAME physical setup as Enzo's PhotonTest, expressed as a generated
# mini-ramses namelist: hydrogen-only gas at n_H = 1e-3 cm⁻³ held isothermal at
# 1e4 K (`neq_TConst`), a 5e48 photons/s monochromatic 13.6 eV point source in
# the corner of a 6.6 kpc box, gas frozen (`static_gas`).  Where Moray traces
# adaptive rays at infinite signal speed, RAMSES-RT advects an M1 photon fluid
# at a REDUCED speed of light — two genuinely different RT methods on one
# density field, which is exactly what the Phase-4 cross-check gate wants.
#
# Units: kpc–m_p–Myr (`UNITS_PARAMS`), so code time is Myr and code density is
# H/cc — the same numbers PhotonTest uses.

